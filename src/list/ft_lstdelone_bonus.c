#include "lft_list.h"
#include <stdlib.h>

/**
 * Deletes the given node, by using the given del function
 * and freeing the node afterward.
 *
 * @param lst Node to delete.
 * @param del Function to delete the node's content.
 */
void	ft_lstdelone(t_list *lst, void (*del)(void *))
{
	if (!lst)
		return ;
	if (del)
		del(lst->content);
	free(lst);
}
